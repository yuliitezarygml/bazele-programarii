# Программа: Сумма и произведение двух чисел

try:
    # Ввод данных
    a = float(input("Введите первое число: "))
    b = float(input("Введите второе число: "))

    # Вычисления
    suma = a + b
    produs = a * b

    # Вывод результатов
    print("Сумма =", suma)
    print("Произведение =", produs)

except ValueError:
    print("Ошибка: нужно ввести числа (например: 3.5, -2, 0.01).")
